package com.bashynskiy.app.utils.convertors;

public class PrimitiveConvertor {

    public static char floatToChar(float float1) {
        return (char) float1;
    }

    public static char intToChar(int int1) {
        return (char) int1;
    }

    public static int charToInt(char char1) {
        return (int) char1;
    }

}
